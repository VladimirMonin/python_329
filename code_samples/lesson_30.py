class Cities:
    """
    –ö–ª–∞—Å—Å Cities
    __init__(self, city_data): –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫–ª–∞—Å—Å–∞ Cities, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –≥–æ—Ä–æ–¥–æ–≤ city_data –∏ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Å–æ—Å—Ç–æ—è–Ω–∏–µ –æ–±—ä–µ–∫—Ç–∞. –í —ç—Ç–æ–º –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –Ω–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≥–æ—Ä–æ–¥–∞–º–∏.
    """

    def __init__(self, cities_list: list):
        self.cities = cities_list

    def get_cities_list(self) -> set:
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å–ø–∏—Å–∫–∞ –≥–æ—Ä–æ–¥–æ–≤
        :

        :return:
        """
        return set(self.cities)


class CityGame:
    def __init__(self, cities: Cities):
        self.cities = cities
        self.human_city: str = ''
        self.computer_city: str = ''

    def check_game_rules(self, last_city: str, new_city: str) -> bool:
        """
        –ú–µ—Ç–æ–¥ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø—Ä–∞–≤–∏–ª –∏–≥—Ä—ã
        :param last_city:
        :param new_city:
        :return:
        """
        if last_city[-1].lower() == new_city[0].lower():
            return True
        else:
            return False

    def human_step(self):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è —Ö–æ–¥–∞ —á–µ–ª–æ–≤–µ–∫–∞
        :return:
        """
        self.human_city = input('–í–≤–µ–¥–∏—Ç–µ –≥–æ—Ä–æ–¥: ')
        if self.human_city == '—Å—Ç–æ–ø':
            print('–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏')
            return False

        if self.human_city not in self.cities.get_cities_list():
            print(f'–ì–æ—Ä–æ–¥–∞ {self.human_city} –Ω–µ—Ç –≤ —Å–ø–∏—Å–∫–µ. –í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏')
            return False

        if self.computer_city:
            if not self.check_game_rules(self.computer_city, self.human_city):
                print(f'–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏. –í–∞—à –æ—Ç–≤–µ—Ç –Ω–µ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –Ω–∞ –±—É–∫–≤—É {self.computer_city[-1]}')
                return False

        self.cities.get_cities_list().remove(self.human_city)
        self.human_city = self.human_city
        return True

    def computer_step(self):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è —Ö–æ–¥–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–∞
        :return:
        """

        for city in self.cities.get_cities_list():
            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤–∏–ª –∏–≥—Ä—ã –º–µ—Ç–æ–¥–æ–º
            if self.check_game_rules(self.human_city, city):
                print(f'–ö–æ–º–ø—å—é—Ç–µ—Ä –Ω–∞–∑—ã–≤–∞–µ—Ç –≥–æ—Ä–æ–¥: {city}')
                self.computer_city = city
                self.cities.get_cities_list().remove(city)
                return True

        else:
            print('–í—ã –ø–æ–±–µ–¥–∏–ª–∏! –ö–æ–º–ø—å—é—Ç–µ—Ä –Ω–µ —Å–º–æ–≥ –Ω–∞–∑–≤–∞—Ç—å –≥–æ—Ä–æ–¥')
            return False


class GameManager:
    def __init__(self):
        self.json_file = JsonFileHandler('../data/cities_set.json')
        self.cities = Cities(self.json_file.read_file())
        self.game = CityGame(self.cities)

    def __call__(self):
        self.run_game()

    def run_game(self):
        while True:
            if not self.game.human_step():
                break
            if not self.game.computer_step():
                break


